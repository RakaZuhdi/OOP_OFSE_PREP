
public class Main {
	public static void main(String[] args) {
		int i = 0;
		System.out.println("While loop:");
		
		while(i < 10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println();
		
		
		System.out.println("Do while loop:");
		int j = 10;		
		do{
			System.out.println(j);
			j++;
		}while(j < 10);
	}
}
